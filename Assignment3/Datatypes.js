// Boolean
var isTrue = true;
var isFalse = false;
// Any
var dynamicValue = 'Hello';
dynamicValue = 10;
dynamicValue = true;
// Array of strings
var fruits = ['apple', 'banana', 'orange'];
// Homogeneous arrays
var numbers = [1, 2, 3, 4, 5];
var booleans = [true, false, true];
var colours = ['red', 'green', 'blue'];
// Heterogeneous array
var mixedValues = ['Hello', 10, true, 'World', false];
console.log(isTrue);
console.log(isFalse);
console.log(dynamicValue);
console.log(fruits);
console.log(numbers);
console.log(booleans);
console.log(colours);
console.log(mixedValues);
